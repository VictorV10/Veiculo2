  private void carregarMarca(){
        Object[] vetorMarca = daoMarca.consultar().toArray();
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel(vetorMarca);
        comboVeiculos.setModel(dcbm);
    }
  private void limparTela(){
       txtPlaca.setText("");
        txtRenavan.setText("");
        
    }
      public void atualizarTabela(){
    
        java.util.List<Cadastro> lista = daoCadastro.consultar();
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("CodCadastro");
        dtm.addColumn("Placa");
        dtm.addColumn("Renavan");
        dtm.addColumn("CodC");
        
        for (Cadastro cadastro : lista) {
            Object[] linha = {cadastro.getCodCadastro(), cadastro.getPlaca(), cadastro.getRenavan(),cadastro.getModelo()};
            dtm.addRow(linha);
        }
       TabelaVeiculos.setModel(dtm);
    }
  
