    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
      
        String placa = txtPlaca.getText();
        long Renavan = Long.parseLong(txtRenavan.getText());
        Marca marca = (Marca)comboVeiculos.getSelectedItem();
        Cadastro cadastro = new Cadastro(placa, Renavan, marca);
        daoCadastro.incluir(cadastro);
        limparTela();
        JOptionPane.showMessageDialog(this, "Inclu√≠do com Sucesso");
        atualizarTabela();
    }//GEN-LAST:event_btn_SalvarActionPerformed

    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed
        int linha = TabelaVeiculos.getSelectedRow();
    if(linha==-1){
        JOptionPane.showMessageDialog(this,"Selecionar uma linha");
        return ;
    }
     
        int CodCadastro = (int)TabelaVeiculos.getValueAt(linha, 0);
        daoCadastro.excluir(CodCadastro);
        atualizarTabela();
    }//GEN-LAST:event_btn_ExcluirActionPerformed

    private void btn_CarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CarregarActionPerformed
        
        int linha = TabelaVeiculos.getSelectedRow();
        int CodCadastro = (int)TabelaVeiculos.getValueAt(linha, 0);
        String placa = (String)TabelaVeiculos.getValueAt(linha, 1);
        long renavan = (int)TabelaVeiculos.getValueAt(linha, 2);
       
        
        txtPlaca.setText(""+placa);
        txtRenavan.setText(""+renavan);
        txtCod.setText(""+CodCadastro);
    }//GEN-LAST:event_btn_CarregarActionPerformed

    private void btn_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AlterarActionPerformed
      int linha = TabelaVeiculos.getSelectedRow();
    if(linha==-1){
        JOptionPane.showMessageDialog(this,"Selecionar uma linha");
        return ;
    }
        String placa = txtPlaca.getText();
        long Renavan = Long.parseLong(txtRenavan.getText());
        Marca marca = (Marca)comboVeiculos.getSelectedItem();
        Cadastro cadastro = new Cadastro(placa, Renavan, marca);
        daoCadastro.alterar(cadastro);
        limparTela();
        JOptionPane.showMessageDialog(this, "Alterar com Sucesso");
        atualizarTabela();
    }//GEN-LAST:event_btn_AlterarActionPerformed

